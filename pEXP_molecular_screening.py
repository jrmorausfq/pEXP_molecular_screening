def merge_descriptor_blocks(
    meta_df: pd.DataFrame,
    tomocomd_df: pd.DataFrame,
    padel_df: pd.DataFrame,
    rdkit_df: pd.DataFrame,
) -> pd.DataFrame:
    out = meta_df.copy()
    for block_name, block in [
        ("ToMoCoMD", tomocomd_df),
        ("PaDEL", padel_df),
        ("RDKit", rdkit_df),
    ]:
        if "Molecule" not in block.columns:
            fail(f"{block_name} descriptor block does not contain Molecule column.")
        out = out.merge(block, on="Molecule", how="left", suffixes=("", f"_{block_name}"))
    out = make_dataframe_columns_unique(out)
    out.to_csv(MERGED_DESCRIPTORS_CSV, index=False)
    logging.info("Merged descriptor table saved: %s | shape=%s", MERGED_DESCRIPTORS_CSV, out.shape)
    return out


def build_all_descriptors(skip_tomocomd: bool = False, skip_padel: bool = False, padel_threads: int = 2, padel_timeout: int = 60000) -> pd.DataFrame:
    meta_df, mols = collect_sdf_metadata_and_mols()
    write_smiles_file(meta_df, ROOT_DIR / "to_predict.smiles")

    if skip_tomocomd:
        logging.warning("Skipping ToMoCoMD execution by user request.")
        tomocomd_df = meta_df[["Molecule"]].copy()
    else:
        raw_tomocomd = run_tomocomd()
        tomocomd_df = prepare_tomocomd_descriptors(raw_tomocomd, meta_df)

    if skip_padel:
        logging.warning("Skipping PaDEL execution by user request.")
        padel_df = meta_df[["Molecule"]].copy()
    else:
        padel_df = compute_padel_descriptors(meta_df, mols, threads=padel_threads, timeout=padel_timeout)

    rdkit_df = compute_rdkit_descriptors(meta_df, mols)
    return merge_descriptor_blocks(meta_df, tomocomd_df, padel_df, rdkit_df)


