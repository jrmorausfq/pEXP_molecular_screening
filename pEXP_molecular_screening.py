def is_ensemble_model(path: Path) -> bool:
    """Return True for the regression stacking/ensemble model.

    The common Spanish spelling used in these projects is "Ensamble". A
    previous run showed a file named "Ensambe.skl" without the final "l", so
    that typo is accepted here too to avoid treating the ensemble as an
    individual base model.
    """
    return path.stem.lower() in {"ensamble", "ensambe", "ensemble", "stacking", "stacked_ensemble"}


def clean_target_like_features(features: List[str], available_columns: Sequence[str]) -> List[str]:
    """Remove a final target/response entry only when it is clearly target-like.

    Older versions also removed a final entry when it was not present in the
    screening descriptor table. That can silently delete a real descriptor or an
    ensemble base-model prediction. Missing features are now handled later by
    reindexing/imputation, while response-column removal is controlled by the
    estimator n_features_in_ when possible.
    """
    if not features:
        return features
    last = str(features[-1]).strip()
    if last in TARGET_NAME_GUESSES:
        logging.info("Ignoring last .txt/train entry as likely response column: %s", last)
        return features[:-1]
    return features

def read_list_file(path: Path, label: str) -> List[str]:
    if not path.exists():
        fail(f"{label} list not found: {path}")
    names = [line.strip() for line in path.read_text(encoding="utf-8", errors="ignore").splitlines() if line.strip()]
    if not names:
        fail(f"{label} list is empty: {path}")
    return names


