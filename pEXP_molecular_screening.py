def compute_padel_descriptors(
    meta_df: pd.DataFrame,
    mols: Sequence[Any],
    threads: int = 2,
    timeout: int = 60000,
) -> pd.DataFrame:
    padeldescriptor = require_padelpy()
    requested = get_requested_padel_descriptors()
    input_sdf = build_padel_input_sdf(meta_df, mols)
    xml_path = build_atom_pairs_padel_xml()

    logging.info("Computing PaDEL APC2D descriptors with padelpy. Requested descriptors: %s", requested)
    run_padel_atom_pairs_count(
        padeldescriptor=padeldescriptor,
        input_sdf=input_sdf,
        output_csv=PADEL_ALL_DESCRIPTORS_CSV,
        xml_path=xml_path,
        threads=threads,
        timeout=timeout,
    )

    if not PADEL_ALL_DESCRIPTORS_CSV.exists():
        fail("PaDEL finished but the output CSV was not created.")
    if PADEL_ALL_DESCRIPTORS_CSV.stat().st_size == 0:
        fail("PaDEL output CSV was created but is empty.")

    padel_all = pd.read_csv(PADEL_ALL_DESCRIPTORS_CSV)
    padel_all = make_dataframe_columns_unique(padel_all)

    selected_cols, missing = map_requested_to_existing_columns(requested, padel_all.columns)
    if missing:
        available_apc = [str(c) for c in padel_all.columns if str(c).upper().startswith("APC2D")]
        preview = available_apc[:50]
        fail(
            "The requested PaDEL APC2D descriptor(s) were not found: "
            f"{missing}. Available APC2D columns preview: {preview}. "
            "Check that the PaDEL XML enables AtomPairs2DFingerprintCount."
        )
    if not selected_cols:
        fail("None of the requested PaDEL descriptors were found in the PaDEL output.")

    # PaDEL normally writes a Name column. If row count matches, keep the SDF-derived names.
    if len(padel_all) == len(meta_df):
        out = pd.concat(
            [meta_df[["Molecule"]].reset_index(drop=True), padel_all[selected_cols].reset_index(drop=True)],
            axis=1,
        )
    else:
        name_col = None
        for candidate in ["Name", "NAME", "Molecule", "ID"]:
            if candidate in padel_all.columns:
                name_col = candidate
                break
        if name_col is None:
            fail(
                "PaDEL row count differs from the SDF molecule count and no Name column was found. "
                "Cannot align PaDEL descriptors safely."
            )
        out = padel_all[[name_col] + selected_cols].copy().rename(columns={name_col: "Molecule"})

    for col in selected_cols:
        out[col] = pd.to_numeric(out[col], errors="coerce")

    out = make_dataframe_columns_unique(out)
    out.to_csv(PADEL_SELECTED_DESCRIPTORS_CSV, index=False)
    logging.info("Selected PaDEL descriptors saved: %s | shape=%s", PADEL_SELECTED_DESCRIPTORS_CSV, out.shape)
    return out


