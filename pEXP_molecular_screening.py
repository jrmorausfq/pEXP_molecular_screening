def run_tomocomd() -> pd.DataFrame:
    jar_file = TOMOCOMD_DIR / "ToMoCoMD-CARDD_CLI.jar"
    if not jar_file.exists():
        fail(f"ToMoCoMD jar not found: {jar_file}")

    calc_dir = TOMOCOMD_DIR / "Calculation"
    if CLEAR_TOMOCOMD_CALCULATION and calc_dir.exists():
        logging.info("Removing previous ToMoCoMD Calculation folder: %s", calc_dir)
        shutil.rmtree(calc_dir)

    logging.info("Running ToMoCoMD CLI in: %s", TOMOCOMD_DIR)
    proc = sp.run(
        ["java", "-jar", str(jar_file.name)],
        cwd=str(TOMOCOMD_DIR),
        text=True,
        stdout=sp.PIPE,
        stderr=sp.STDOUT,
        check=False,
    )
    logging.info("ToMoCoMD return code: %s", proc.returncode)
    if proc.stdout:
        logging.info("ToMoCoMD output:\n%s", proc.stdout)
    if proc.returncode != 0:
        fail("ToMoCoMD execution failed. Check Screening_Results/screening_log.txt")

    csv_files = sorted(
        calc_dir.glob("**/*user_specified_headings*.csv"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    if not csv_files:
        fail("No ToMoCoMD user_specified_headings CSV was found after execution.")

    dfs: List[pd.DataFrame] = []
    for csv_path in csv_files:
        try:
            df = pd.read_csv(csv_path)
            df["source_tomocomd_csv"] = str(csv_path.relative_to(TOMOCOMD_DIR))
            dfs.append(df)
            logging.info("ToMoCoMD CSV loaded: %s | shape=%s", csv_path, df.shape)
        except Exception as exc:
            logging.warning("Could not read ToMoCoMD CSV %s: %s", csv_path, exc)

    if not dfs:
        fail("ToMoCoMD ran, but no readable descriptor CSV was obtained.")

    return make_dataframe_columns_unique(pd.concat(dfs, axis=0, ignore_index=True))


def clean_tomocomd_molecule_name(value: Any) -> str:
    text = str(value).strip().replace("\\", "/")
    last = text.split("/")[-1]
    if last.lower().endswith(".sdf"):
        last = Path(last).stem
    return last or text


def prepare_tomocomd_descriptors(tomocomd_df: pd.DataFrame, meta_df: pd.DataFrame) -> pd.DataFrame:
    df = tomocomd_df.copy()
    if "molecules" in df.columns:
        tomocomd_names = df["molecules"].map(clean_tomocomd_molecule_name)
    elif "Molecule" in df.columns:
        tomocomd_names = df["Molecule"].astype(str)
    else:
        tomocomd_names = pd.Series([f"Molecule_{i + 1}" for i in range(len(df))])

    if "Molecule" in df.columns:
        df = df.drop(columns=["Molecule"])

    if len(df) == len(meta_df):
        df.insert(0, "Molecule", meta_df["Molecule"].values)
    else:
        df.insert(0, "Molecule", tomocomd_names.values)
        logging.warning(
            "ToMoCoMD rows (%s) and SDF molecules (%s) differ. ToMoCoMD names will be used.",
            len(df), len(meta_df),
        )

    drop_cols = [c for c in ["molecules", "source_tomocomd_csv"] if c in df.columns]
    if drop_cols:
        df = df.drop(columns=drop_cols)

    df = make_dataframe_columns_unique(df)
    df.to_csv(TOMOCOMD_DESCRIPTORS_CSV, index=False)
    return df


