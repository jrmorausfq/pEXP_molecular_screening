def build_rdkit_descriptor_function_map(selected_names: Sequence[str]) -> Dict[str, Any]:
    require_rdkit()
    requested_raw = {str(x).lower() for x in selected_names}
    requested_san = {sanitize_name(x).lower() for x in selected_names}

    funcs: Dict[str, Any] = {}
    for rdkit_name, func in Descriptors.descList:
        raw_key = str(rdkit_name).lower()
        san_key = sanitize_name(rdkit_name).lower()
        if raw_key in requested_raw or san_key in requested_san:
            funcs[sanitize_name(rdkit_name)] = func

    missing = []
    matched = {k.lower() for k in funcs.keys()}
    for requested_name in selected_names:
        if sanitize_name(requested_name).lower() not in matched:
            missing.append(str(requested_name))
    if missing:
        logging.warning("RDKit descriptors not matched and ignored: %s", missing)
    if not funcs:
        fail("None of the requested RDKit descriptors matched RDKit Descriptors.descList.")
    return funcs


def compute_rdkit_descriptors(meta_df: pd.DataFrame, mols: List[Any]) -> pd.DataFrame:
    selected = read_list_file(RDKIT_DESCRIPTOR_FILE, "RDKit descriptor")
    funcs = build_rdkit_descriptor_function_map(selected)
    logging.info("Computing selected RDKit descriptors: %s", len(funcs))

    rows: List[Dict[str, Any]] = []
    for mol in mols:
        values: Dict[str, Any] = {}
        if mol is None:
            for name in funcs:
                values[name] = np.nan
        else:
            mol_no_h = Chem.RemoveHs(mol)
            for name, func in funcs.items():
                try:
                    values[name] = func(mol_no_h)
                except Exception:
                    values[name] = np.nan
        rows.append(values)

    out = pd.concat([meta_df[["Molecule"]].reset_index(drop=True), pd.DataFrame(rows)], axis=1)
    out = make_dataframe_columns_unique(out)
    out.to_csv(RDKIT_DESCRIPTORS_CSV, index=False)
    return out


