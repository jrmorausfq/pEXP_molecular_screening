def find_sdf_files() -> List[Path]:
    if not CHEMICAL_DATASETS_DIR.exists():
        fail(f"Folder not found: {CHEMICAL_DATASETS_DIR}")
    sdf_files = sorted(CHEMICAL_DATASETS_DIR.glob("*.sdf"), key=lambda p: p.name.lower())
