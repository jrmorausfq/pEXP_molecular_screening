def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Molecular screening for pEXP sklearn regression ensemble and ALTA/BAJA classification models.")
    parser.add_argument("--skip-tomocomd", action="store_true", help="Skip ToMoCoMD. Useful only for debugging.")
    parser.add_argument("--skip-padel", action="store_true", help="Skip PaDEL. Useful only for debugging.")
    parser.add_argument("--keep-old-tomocomd-calculation", action="store_true", help="Do not remove ToMoCoMD/Calculation before running.")
    parser.add_argument("--descriptors-only", action="store_true", help="Generate descriptors_merged.csv but do not calculate AD or predictions.")
    parser.add_argument("--use-existing-descriptors", action="store_true", help="Resume from Screening_Results/descriptors_merged.csv instead of recalculating descriptors.")
    parser.add_argument("--in-memory", action="store_true", help="Use the old in-memory AD/prediction path. Not recommended for >100k molecules.")
    parser.add_argument("--chunk-size", type=int, default=DEFAULT_CHUNK_SIZE, help="Rows per chunk for AD and prediction. Default: 50000.")
    parser.add_argument("--ad-n-jobs", type=int, default=1, help="Parallel jobs for sklearn NearestNeighbors in chunked AD. Keep 1 to reduce memory.")
    parser.add_argument("--ad-only", action="store_true", help="Generate descriptors and AD CSVs but do not run predictions.")
    parser.add_argument("--allow-missing-ad", action="store_true", help="Continue prediction if AD CSV file is missing or AD fails. Not recommended.")
    parser.add_argument("--ad-training-csv", default=None, help="Path to the single common AD training CSV. Default: training_AD.csv")
    parser.add_argument("--padel-threads", type=int, default=max(1, int(os.environ.get("SLURM_CPUS_PER_TASK", "2"))), help="Number of PaDEL threads.")
    parser.add_argument("--padel-timeout", type=int, default=60000, help="Maximum PaDEL runtime per molecule in ms.")
    parser.add_argument("--positive-class", default=DEFAULT_POSITIVE_CLASS, help="Class used as positive for Score/DP in classification. Default: ALTA.")
    parser.add_argument("--ignore-model-train-csv", action="store_true", help="Ignore optional *_train.csv files beside the models. By default they are used for feature order, medians, and classification audits.")
    parser.add_argument("--no-audit", action="store_true", help="Do not write diagnostic/audit CSV files. Final prediction files are still generated.")
    parser.add_argument("--final-only", action="store_true", help="After a successful run, keep only results_reg.csv, results_class.csv, molecules_outside_AD.csv, and screening_log.txt in Screening_Results.")
    parser.add_argument("--quiet", action="store_true", help="Reduce console output. Full details are still written to screening_log.txt unless --final-only removes intermediate files.")
    return parser.parse_args()


def cleanup_final_only_outputs() -> None:
    """Remove intermediate and audit files after a successful production run."""
    if not RESULTS_DIR.exists():
        return
    for path in RESULTS_DIR.iterdir():
        if path.is_file() and path.name not in FINAL_ONLY_KEEP_NAMES:
            try:
                path.unlink()
            except Exception as exc:
                logging.warning("Could not remove intermediate file %s: %s", path, exc)


def print_final_summary(start_time: float, final_only: bool = False) -> None:
    elapsed = (time.time() - start_time) / 60.0
    logging.info("=" * 80)
    logging.info("Screening completed in %.2f min", elapsed)
    summary_paths = [
        RESULTS_REG_CSV,
        RESULTS_CLASS_CSV,
        OUTSIDE_AD_CSV,
        LOG_FILE,
    ] if final_only else [
        MERGED_DESCRIPTORS_CSV,
        TOMOCOMD_DESCRIPTORS_CSV,
        PADEL_SELECTED_DESCRIPTORS_CSV,
        RDKIT_DESCRIPTORS_CSV,
        AD_COMMON_CSV,
        AD_ORDERED_SCREENING_CSV,
        DESCRIPTORS_IN_AD_CSV,
        OUTSIDE_AD_CSV,
        INDIVIDUAL_REGRESSION_CSV,
        ENSEMBLE_INPUT_CSV,
        REGRESSION_MODEL_FEATURE_AUDIT_CSV,
        CLASSIFICATION_MODEL_FEATURE_AUDIT_CSV,
        CLASSIFICATION_LABEL_AUDIT_CSV,
        CLASSIFICATION_TRAIN_SELF_CHECK_CSV,
        CLASSIFICATION_TRAIN_MATCH_AUDIT_CSV,
        RESULTS_REG_CSV,
        RESULTS_CLASS_CSV,
        LOG_FILE,
    ]
    for path in summary_paths:
        if path.exists():
            logging.info("  %s", path)
    logging.info("=" * 80)


def main() -> None:
    global CLEAR_TOMOCOMD_CALCULATION, WRITE_AUDIT_FILES
    args = parse_args()
    if args.keep_old_tomocomd_calculation:
        CLEAR_TOMOCOMD_CALCULATION = False
    if args.no_audit or args.final_only:
        WRITE_AUDIT_FILES = False

    setup_logging(quiet_console=args.quiet)
    start_time = time.time()
    logging.info("Starting pEXP molecular screening workflow.")
    logging.info("Project root: %s", ROOT_DIR)
    logging.info("Molecules folder: %s", CHEMICAL_DATASETS_DIR)
    logging.info("Regression models folder: %s", REGRESSION_MODELS_DIR)
    logging.info("Regression individual models folder: %s", REGRESSION_INDIVIDUAL_MODELS_DIR)
    logging.info("Classification models folder: %s", CLASSIFICATION_MODELS_DIR)
    logging.info("Common AD training CSV: %s", args.ad_training_csv if args.ad_training_csv else TRAINING_AD_FILE)

    if args.use_existing_descriptors:
        if not MERGED_DESCRIPTORS_CSV.exists():
            fail(f"--use-existing-descriptors was requested, but this file does not exist: {MERGED_DESCRIPTORS_CSV}")
        logging.info("Using existing merged descriptors: %s", MERGED_DESCRIPTORS_CSV)
    else:
        descriptors_df = build_all_descriptors(
            skip_tomocomd=args.skip_tomocomd,
            skip_padel=args.skip_padel,
            padel_threads=args.padel_threads,
            padel_timeout=args.padel_timeout,
        )
        # The descriptor table is persisted to descriptors_merged.csv. Free the
        # in-memory copy before AD/prediction to avoid RAM spikes on >1M rows.
        del descriptors_df
        gc.collect()

    if args.descriptors_only:
        logging.info("Descriptor-only mode requested. AD and predictions will not be run.")
        print_final_summary(start_time, final_only=args.final_only)
        return

    require_ad = REQUIRE_AD_BEFORE_PREDICTION and not args.allow_missing_ad
    use_model_train_csv = not args.ignore_model_train_csv

    if not args.in_memory:
        run_large_screening_from_merged_csv(
            require_ad=require_ad,
            training_csv=args.ad_training_csv,
            chunk_size=args.chunk_size,
            ad_only=args.ad_only,
            allow_missing_ad=args.allow_missing_ad,
            positive_class=args.positive_class,
            use_model_train_csv=use_model_train_csv,
            ad_n_jobs=args.ad_n_jobs,
        )
        if args.ad_only:
            logging.info("AD-only mode requested. Predictions were not run.")
            print_final_summary(start_time, final_only=args.final_only)
            return
    else:
        logging.warning("Running old in-memory AD/prediction mode. This is not recommended for very large screenings.")
        descriptors_df = pd.read_csv(MERGED_DESCRIPTORS_CSV, low_memory=False)
        common_ad_df = evaluate_common_applicability_domain(
            descriptors_df,
            require_ad=require_ad,
            training_csv=args.ad_training_csv,
        )

        if common_ad_df is not None:
            common_ad_df.to_csv(AD_COMMON_CSV, index=False)

        if args.ad_only:
            logging.info("AD-only mode requested. Predictions will not be run.")
            print_final_summary(start_time, final_only=args.final_only)
            return

        descriptors_in = filter_common_in_domain(descriptors_df, common_ad_df, allow_missing_ad=args.allow_missing_ad)
        run_regression_screening(descriptors_in, common_ad_df, use_model_train_csv=use_model_train_csv)
        run_classification_screening(
            descriptors_in,
            common_ad_df,
            positive_class=args.positive_class,
            use_model_train_csv=use_model_train_csv,
        )
    if args.final_only:
        cleanup_final_only_outputs()
    print_final_summary(start_time, final_only=args.final_only)


if __name__ == "__main__":
    main()
