def collect_sdf_metadata_and_mols() -> Tuple[pd.DataFrame, List[Any]]:
    require_rdkit()
    sdf_files = find_sdf_files()
    rows: List[Dict[str, Any]] = []
    mols: List[Any] = []
    seen_names: Dict[str, int] = {}

    for sdf_path in sdf_files:
        supplier = Chem.SDMolSupplier(str(sdf_path), removeHs=False, sanitize=True)
        if supplier is None:
            logging.warning("Could not open SDF with RDKit: %s", sdf_path)
            continue

        for i, mol in enumerate(supplier, start=1):
            if mol is None:
                logging.warning("Skipping invalid molecule %s entry %s", sdf_path.name, i)
                continue

            try:
                raw_name = mol.GetProp("_Name").strip()
            except Exception:
                raw_name = ""
            name = raw_name if raw_name else f"{sdf_path.stem}_{i}"
            if name in seen_names:
                seen_names[name] += 1
                name = f"{name}__{seen_names[name]}"
            else:
                seen_names[name] = 1

            try:
                smiles = Chem.MolToSmiles(Chem.RemoveHs(mol), isomericSmiles=True)
            except Exception:
                smiles = ""

            rows.append({"Molecule": name, "SMILES": smiles, "source_sdf": sdf_path.name})
            mols.append(mol)

    if not rows:
        fail("No valid molecules could be read from the SDF files.")

    meta = pd.DataFrame(rows)
    logging.info("Valid molecules read from SDF: %s", len(meta))
    return meta, mols


def write_smiles_file(meta_df: pd.DataFrame, output_file: Path) -> Path:
    with output_file.open("w", encoding="utf-8") as f:
        for _, row in meta_df.iterrows():
            smiles = str(row.get("SMILES", "")).strip()
            name = str(row.get("Molecule", "")).strip()
            if smiles:
                f.write(f"{smiles} {name}\n")
    logging.info("SMILES file generated: %s", output_file)
    return output_file


