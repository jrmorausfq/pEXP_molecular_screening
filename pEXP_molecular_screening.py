def sanitize_name(name: Any) -> str:
    text = str(name)
    return "".join(c if c.isalnum() or c == "_" else "_" for c in text)


def make_unique(values: Sequence[str]) -> List[str]:
    seen: Dict[str, int] = {}
    out: List[str] = []
    for value in values:
        base = str(value)
        if base not in seen:
            seen[base] = 1
            out.append(base)
        else:
            seen[base] += 1
            out.append(f"{base}__dup{seen[base]}")
    return out


def make_dataframe_columns_unique(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()
    out.columns = make_unique([str(c).strip() for c in out.columns])
    return out


