def append_dataframe_csv(df: pd.DataFrame, path: Path, first_chunk: bool = False) -> None:
    """Append a DataFrame to CSV while writing the header only once.

    This is used by the large-screening mode to avoid accumulating millions of
    rows in memory before writing final outputs.
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    write_header = first_chunk or (not path.exists()) or path.stat().st_size == 0
    df.to_csv(path, mode="w" if write_header else "a", header=write_header, index=False)


