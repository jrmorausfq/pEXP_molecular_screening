def align_feature_list_to_expected_count(
    feature_columns: List[str],
    source_df: pd.DataFrame,
    estimator: Any,
    model_path: Path,
) -> List[str]:
    """Drop a final response column only when feature count indicates it.

    If a .txt contains descriptors plus the response in the final column, the
    estimator generally reports one fewer input feature than the .txt length.
    In that specific case the final entry is removed. If the length already
    equals n_features_in_, nothing is removed. This protects ensemble inputs
    where the last column is a real base-model prediction.
    """
    expected = get_expected_n_features(estimator)
    if expected is None:
        return feature_columns

    if len(feature_columns) == expected + 1:
        last = str(feature_columns[-1]).strip()
        source_cols = set(map(str, source_df.columns))
        if last in TARGET_NAME_GUESSES or last not in source_cols:
            logging.info(
                "%s: ignoring final .txt/bundle entry %r to match estimator n_features_in_=%s.",
                model_path.name, last, expected,
            )
            return feature_columns[:-1]

    if len(feature_columns) != expected:
        logging.warning(
            "%s: selected %s feature name(s), but estimator reports n_features_in_=%s. "
            "The script will preserve the selected feature list; prediction will fail if this is inconsistent.",
            model_path.name, len(feature_columns), expected,
        )
    return feature_columns


def get_training_medians(bundle: Dict[str, Any], feature_columns: Sequence[str]) -> Optional[pd.Series]:
    med = bundle.get("train_medians")
    if med is None:
        return None
    if isinstance(med, pd.Series):
        return med.reindex(feature_columns)
    if isinstance(med, dict):
        return pd.Series(med).reindex(feature_columns)
    try:
        return pd.Series(med, index=feature_columns)
    except Exception:
        return None


def select_features_for_model(
    source_df: pd.DataFrame,
    model_path: Path,
    estimator: Any,
    bundle: Dict[str, Any],
    allow_dummies: bool = True,
    fail_on_missing: bool = False,
    model_train_csv: Optional[Path] = None,
) -> pd.DataFrame:
    df = make_dataframe_columns_unique(source_df)
    non_id_cols = descriptor_columns(df)
    base_df = df[non_id_cols].copy()

    feature_columns: Optional[List[str]] = None
    source_label = "fallback_all_non_id_columns"
    use_dummies = False

    if model_train_csv is not None and model_train_csv.exists():
        try:
            train_features, train_target, train_notes = training_csv_feature_columns(model_train_csv, estimator, model_path)
            if train_features:
                feature_columns = [str(x) for x in train_features]
                source_label = f"model_training_csv:{model_train_csv.name}"
                use_dummies = False
                logging.info(
                    "%s: using feature order from training CSV %s. Target column inferred: %s. Notes: %s",
                    model_path.name,
                    model_train_csv.name,
                    train_target if train_target else "<none>",
                    " | ".join(train_notes),
                )
        except Exception as exc:
            logging.warning("%s: could not use training CSV %s for feature order: %s", model_path.name, model_train_csv, exc)

    if feature_columns is None:
        bundle_features = get_bundle_feature_columns(bundle)
        if bundle_features:
            feature_columns = bundle_features
            source_label = "joblib_bundle_feature_columns"
            use_dummies = allow_dummies
        else:
            txt_entries = read_model_txt_entries(model_path)
            if txt_entries:
                numeric_indexes = parse_numeric_index_entries(txt_entries)
                if numeric_indexes is not None:
                    feature_columns = convert_index_entries_to_columns(numeric_indexes, non_id_cols, model_path)
                    source_label = "txt_numeric_indexes"
                    use_dummies = False
                else:
                    feature_columns = [str(x).strip() for x in txt_entries]
                    feature_columns = clean_target_like_features(feature_columns, df.columns)
                    source_label = "txt_descriptor_names"
                    use_dummies = False

    if feature_columns is None:
        estimator_features = get_estimator_feature_names(estimator)
        if estimator_features:
            feature_columns = estimator_features
            source_label = "estimator_feature_names_in"
            use_dummies = allow_dummies

    if feature_columns is None:
        feature_columns = non_id_cols

    feature_columns = clean_target_like_features([str(c) for c in feature_columns], df.columns)
    feature_columns = align_feature_list_to_expected_count(feature_columns, df, estimator, model_path)
    if not feature_columns:
        fail(f"No feature columns could be determined for model: {model_path}")

    if use_dummies:
        X_base = pd.get_dummies(base_df, drop_first=False)
        X_base = X_base.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)
        missing = [c for c in feature_columns if c not in X_base.columns]
        if missing:
            msg = (
                f"{model_path.name}: {len(missing)} feature(s) not found after dummy expansion: "
                f"{missing[:20]}{' ...' if len(missing) > 20 else ''}"
            )
            if fail_on_missing:
                fail(msg)
            logging.warning("%s. They will be imputed.", msg)
        X = X_base.reindex(columns=feature_columns, fill_value=np.nan)
    else:
        missing = [c for c in feature_columns if c not in df.columns]
        if missing:
            msg = (
                f"{model_path.name}: {len(missing)} descriptor(s) not found: "
                f"{missing[:20]}{' ...' if len(missing) > 20 else ''}"
            )
            if fail_on_missing:
                fail(msg)
            logging.warning("%s. They will be imputed.", msg)
        X = df.reindex(columns=feature_columns, fill_value=np.nan)
        X = X.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)

    medians = training_csv_medians(model_train_csv, feature_columns) if model_train_csv is not None else None
    if medians is None:
        medians = get_training_medians(bundle, feature_columns)
    if medians is not None:
        X = X.fillna(medians).fillna(DEFAULT_FILL_VALUE)
    else:
        X = X.fillna(DEFAULT_FILL_VALUE)

    logging.info("%s: using %s features from %s.", model_path.name, X.shape[1], source_label)
    return X


