def find_ensemble_model() -> Optional[Path]:
    """Find the regression ensemble model in models/regression/.

    Individual base learners must be placed in models/regression/individuals/.
    The ensemble remains directly inside models/regression/.
    """
    preferred_names = [
        "Ensamble.skl", "ensamble.skl",
        "Ensambe.skl", "ensambe.skl",
        "Ensemble.skl", "ensemble.skl",
        "Stacking.skl", "stacking.skl",
        "stacked_ensemble.skl",
    ]
    for name in preferred_names:
        candidate = REGRESSION_MODELS_DIR / name
        if candidate.exists() and candidate.is_file():
            return candidate

    root_skl = ordered_skl_files(REGRESSION_MODELS_DIR)
    for path in root_skl:
        if is_ensemble_model(path):
            return path
    return None


def ordered_individual_regression_models() -> List[Path]:
    if not REGRESSION_INDIVIDUAL_MODELS_DIR.exists():
        fail(
            "Regression individual models folder not found. Expected: "
            f"{REGRESSION_INDIVIDUAL_MODELS_DIR}. Put the 9 base .skl models and their .txt files there."
        )
    model_files = ordered_skl_files(REGRESSION_INDIVIDUAL_MODELS_DIR)
    model_files = [p for p in model_files if not is_ensemble_model(p)]
    if not model_files:
        fail(
            "No individual regression .skl models found in: "
            f"{REGRESSION_INDIVIDUAL_MODELS_DIR}. The ensemble must stay outside this folder."
        )
    return model_files


def run_regression_screening(
    descriptors_in_df: pd.DataFrame,
    regression_ad_df: Optional[pd.DataFrame],
    use_model_train_csv: bool = True,
    append_results: bool = False,
    chunk_index: int = 0,
) -> pd.DataFrame:
    individual_models = ordered_individual_regression_models()
    ensemble_model = find_ensemble_model()
    if ensemble_model is None:
        fail(
            "No regression ensemble model was found directly in models/regression/. "
            "Expected one of: Ensamble.skl, Ensambe.skl, Ensemble.skl."
        )

    pred_df = descriptors_in_df[["Molecule"]].copy()
    if "SMILES" in descriptors_in_df.columns:
        pred_df["SMILES"] = descriptors_in_df["SMILES"].values

    feature_audit_rows: List[Dict[str, Any]] = []
    individual_pred_cols: List[str] = []
    logging.info("Regression individual models folder: %s", REGRESSION_INDIVIDUAL_MODELS_DIR)
    logging.info("Regression ensemble model: %s", ensemble_model)

    for model_path in individual_models:
        model_name = model_path.stem
        logging.info("Predicting individual regression model: %s", model_path.name)
        estimator, bundle = load_skl(model_path)
        model_train_csv = find_model_training_csv(model_path) if use_model_train_csv else None
        if model_train_csv is not None:
            logging.info("%s: matched individual regression training CSV: %s", model_path.name, model_train_csv)
        X = select_features_for_model(
            descriptors_in_df,
            model_path,
            estimator,
            bundle,
            allow_dummies=True,
            model_train_csv=model_train_csv,
        )
        try:
            pred = estimator.predict(X)
        except Exception as exc:
            fail(f"Prediction failed for individual regression model {model_path.name}: {exc}")
        pred_df[model_name] = np.asarray(pred).reshape(-1)
        individual_pred_cols.append(model_name)
        feature_audit_rows.append({
            "ModelType": "individual_regression",
            "ModelFile": str(model_path),
            "OutputColumn": model_name,
            "NFeaturesUsed": X.shape[1],
            "ExpectedNFeatures": get_expected_n_features(estimator),
            "TrainingCSVUsed": str(model_train_csv) if model_train_csv is not None else "",
            "FeatureSourceColumns": ";".join(map(str, X.columns.tolist())),
        })

    append_dataframe_csv(pred_df, INDIVIDUAL_REGRESSION_CSV, first_chunk=(not append_results or chunk_index == 0))
    logging.info("Individual regression predictions saved/appended: %s", INDIVIDUAL_REGRESSION_CSV)

    # The ensemble input is built from the individual predictions. Its column
    # order is determined by the ensemble bundle/.txt/feature_names_in_, not by
    # alphabetical order, so it matches the scaler/model used during training.
    ensemble_source_df = pred_df[["Molecule"] + individual_pred_cols].copy()
    logging.info("Predicting regression ensemble model: %s", ensemble_model.name)
    estimator, bundle = load_skl(ensemble_model)
    ensemble_train_csv = find_model_training_csv(ensemble_model) if use_model_train_csv else None
    if ensemble_train_csv is not None:
        logging.info("%s: matched ensemble training CSV: %s", ensemble_model.name, ensemble_train_csv)
    X_ens = select_features_for_model(
        ensemble_source_df,
        ensemble_model,
        estimator,
        bundle,
        allow_dummies=True,
        fail_on_missing=True,
        model_train_csv=ensemble_train_csv,
    )
    ensemble_audit_df = pd.concat(
        [pred_df[[c for c in ["Molecule", "SMILES"] if c in pred_df.columns]].reset_index(drop=True), X_ens.reset_index(drop=True)],
        axis=1,
    )
    append_dataframe_csv(ensemble_audit_df, ENSEMBLE_INPUT_CSV, first_chunk=(not append_results or chunk_index == 0))
    logging.info("Ordered ensemble input predictions saved/appended: %s", ENSEMBLE_INPUT_CSV)

    try:
        ens_pred = estimator.predict(X_ens)
    except Exception as exc:
        fail(f"Prediction failed for ensemble regression model {ensemble_model.name}: {exc}")
    pred_df["pEXP_Ensamble"] = np.asarray(ens_pred).reshape(-1)
    feature_audit_rows.append({
        "ModelType": "regression_ensemble",
        "ModelFile": str(ensemble_model),
        "OutputColumn": "pEXP_Ensamble",
        "NFeaturesUsed": X_ens.shape[1],
        "ExpectedNFeatures": get_expected_n_features(estimator),
        "TrainingCSVUsed": str(ensemble_train_csv) if ensemble_train_csv is not None else "",
        "FeatureSourceColumns": ";".join(map(str, X_ens.columns.tolist())),
    })

    if WRITE_AUDIT_FILES and (not append_results or chunk_index == 0):
        pd.DataFrame(feature_audit_rows).to_csv(REGRESSION_MODEL_FEATURE_AUDIT_CSV, index=False)
        logging.info("Regression model feature audit saved: %s", REGRESSION_MODEL_FEATURE_AUDIT_CSV)

    pred_df = merge_ad_to_result(pred_df, regression_ad_df)
    id_cols = [c for c in ["Molecule", "SMILES"] if c in pred_df.columns]
    priority = [c for c in ["pEXP_Ensamble", "AD_InDomain", "AD_OutCount"] if c in pred_df.columns]
    other = [c for c in pred_df.columns if c not in id_cols + priority]
    pred_df = pred_df[id_cols + priority + other]
    append_dataframe_csv(pred_df, RESULTS_REG_CSV, first_chunk=(not append_results or chunk_index == 0))
    logging.info("Regression results saved/appended: %s", RESULTS_REG_CSV)
    return pred_df


