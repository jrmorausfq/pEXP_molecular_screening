def prepare_ad_numeric_matrices(X_train_raw: pd.DataFrame, X_test_raw: pd.DataFrame) -> Tuple[pd.DataFrame, pd.DataFrame, pd.Series, List[str]]:
    train = make_dataframe_columns_unique(X_train_raw).copy()
    test = make_dataframe_columns_unique(X_test_raw).copy()

    train = train.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)
    test = test.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)

    train_medians = train.median(numeric_only=True).fillna(DEFAULT_FILL_VALUE)
    train = train.fillna(train_medians).fillna(DEFAULT_FILL_VALUE)
    test = test.reindex(columns=train.columns, fill_value=np.nan)
    test = test.fillna(train_medians).fillna(DEFAULT_FILL_VALUE)

    # Drop constant descriptors based only on training data.
    keep_cols = train.columns[train.nunique(dropna=False) > 1].tolist()
    train = train[keep_cols].copy()
    test = test[keep_cols].copy()
    return train, test, train_medians, keep_cols


def scale_for_distance(X_train: pd.DataFrame, X_test: pd.DataFrame) -> Tuple[np.ndarray, np.ndarray]:
    mean = X_train.mean(axis=0).to_numpy(dtype=float)
    std = X_train.std(axis=0, ddof=0).replace(0, 1).to_numpy(dtype=float)
    z_train = (X_train.to_numpy(dtype=float) - mean) / std
    z_test = (X_test.to_numpy(dtype=float) - mean) / std
    return z_train, z_test


def min_distance_to_train(z_train: np.ndarray, z_test: np.ndarray, metric: str, batch_size: int = AD_BATCH_SIZE) -> np.ndarray:
    out: List[np.ndarray] = []
    for start in range(0, z_test.shape[0], batch_size):
        end = min(start + batch_size, z_test.shape[0])
        diff = z_test[start:end, None, :] - z_train[None, :, :]
        if metric == "cityblock":
            dist = np.abs(diff).sum(axis=2)
        else:
            dist = np.sqrt((diff ** 2).sum(axis=2))
        out.append(np.min(dist, axis=1))
    return np.concatenate(out) if out else np.array([])


def kth_distance_to_train(z_train: np.ndarray, z_test: np.ndarray, k: int, metric: str, batch_size: int = AD_BATCH_SIZE) -> np.ndarray:
    k = max(1, min(k, z_train.shape[0]))
    out: List[np.ndarray] = []
    for start in range(0, z_test.shape[0], batch_size):
        end = min(start + batch_size, z_test.shape[0])
        diff = z_test[start:end, None, :] - z_train[None, :, :]
        if metric == "cityblock":
            dist = np.abs(diff).sum(axis=2)
        else:
            dist = np.sqrt((diff ** 2).sum(axis=2))
        kth = np.partition(dist, kth=k - 1, axis=1)[:, k - 1]
        out.append(kth)
    return np.concatenate(out) if out else np.array([])


def training_neighbor_distances(z_train: np.ndarray, metric: str, k: int, batch_size: int = AD_BATCH_SIZE) -> np.ndarray:
    n = z_train.shape[0]
    if n < 2 or z_train.size == 0:
        return np.full(n, np.nan)
    k = max(1, min(k, n - 1))
    out = np.zeros(n, dtype=float)
    for start in range(0, n, batch_size):
        end = min(start + batch_size, n)
        diff = z_train[start:end, None, :] - z_train[None, :, :]
        if metric == "cityblock":
            dist = np.abs(diff).sum(axis=2)
        else:
            dist = np.sqrt((diff ** 2).sum(axis=2))
        for row_idx, global_idx in enumerate(range(start, end)):
            dist[row_idx, global_idx] = np.inf
        out[start:end] = np.partition(dist, kth=k - 1, axis=1)[:, k - 1]
    return out


def safe_percentile(values: np.ndarray, percentile: float) -> float:
    vals = np.asarray(values, dtype=float)
    vals = vals[np.isfinite(vals)]
    if vals.size == 0:
        return np.nan
    return float(np.percentile(vals, percentile))


def compute_applicability_domain(X_train_raw: pd.DataFrame, X_test_raw: pd.DataFrame, molecule_names: Sequence[Any]) -> pd.DataFrame:
    X_train, X_test, _, _ = prepare_ad_numeric_matrices(X_train_raw, X_test_raw)
    n_test = len(X_test_raw)

    if X_train.shape[0] < 2 or X_train.shape[1] < 1:
        logging.warning("AD could not be calculated rigorously: training rows=%s, variable descriptors=%s", X_train.shape[0], X_train.shape[1])
        return pd.DataFrame({
            "Molecule": list(molecule_names),
            "AD_InDomain": [np.nan] * n_test,
            "AD_OutCount": [np.nan] * n_test,
            "AD_RANGE_Out": [np.nan] * n_test,
            "AD_EUCLIDEAN_Out": [np.nan] * n_test,
            "AD_CITYBLOCK_Out": [np.nan] * n_test,
            "AD_DENSITY_Out": [np.nan] * n_test,
        })

    train_min = X_train.min(axis=0)
    train_max = X_train.max(axis=0)
    inside = X_test.ge(train_min, axis=1) & X_test.le(train_max, axis=1)
    range_fraction = inside.mean(axis=1).to_numpy(dtype=float)
    range_out = range_fraction < AD_RANGE_MIN_FRACTION

    z_train, z_test = scale_for_distance(X_train, X_test)

    eu_train_nn = training_neighbor_distances(z_train, metric="euclidean", k=1)
    eu_threshold = safe_percentile(eu_train_nn, AD_DISTANCE_PERCENTILE)
    eu_test = min_distance_to_train(z_train, z_test, metric="euclidean")
    eu_out = eu_test > eu_threshold if np.isfinite(eu_threshold) else np.full(n_test, np.nan)

    cb_train_nn = training_neighbor_distances(z_train, metric="cityblock", k=1)
    cb_threshold = safe_percentile(cb_train_nn, AD_DISTANCE_PERCENTILE)
    cb_test = min_distance_to_train(z_train, z_test, metric="cityblock")
    cb_out = cb_test > cb_threshold if np.isfinite(cb_threshold) else np.full(n_test, np.nan)

    k_density = max(1, min(AD_K_NEIGHBORS, z_train.shape[0] - 1))
    density_train = training_neighbor_distances(z_train, metric="euclidean", k=k_density)
    density_threshold = safe_percentile(density_train, AD_DISTANCE_PERCENTILE)
    density_test = kth_distance_to_train(z_train, z_test, k=k_density, metric="euclidean")
    density_out = density_test > density_threshold if np.isfinite(density_threshold) else np.full(n_test, np.nan)

    flags = pd.DataFrame({
        "AD_RANGE_Out": pd.Series(range_out).astype(float),
        "AD_EUCLIDEAN_Out": pd.Series(eu_out).astype(float),
        "AD_CITYBLOCK_Out": pd.Series(cb_out).astype(float),
        "AD_DENSITY_Out": pd.Series(density_out).astype(float),
    })
    out_count = flags.sum(axis=1, skipna=True)
    valid_counts = flags.notna().sum(axis=1)
    in_domain = np.where(valid_counts >= 1, out_count < AD_OUT_COUNT_THRESHOLD, np.nan)

    return pd.DataFrame({
        "Molecule": list(molecule_names),
        "AD_InDomain": in_domain,
        "AD_OutCount": out_count,
        "AD_RANGE_Out": flags["AD_RANGE_Out"],
        "AD_EUCLIDEAN_Out": flags["AD_EUCLIDEAN_Out"],
        "AD_CITYBLOCK_Out": flags["AD_CITYBLOCK_Out"],
        "AD_DENSITY_Out": flags["AD_DENSITY_Out"],
        "AD_RANGE_FractionInside": range_fraction,
        "AD_EUCLIDEAN_Distance": eu_test,
        "AD_EUCLIDEAN_Threshold": eu_threshold,
        "AD_CITYBLOCK_Distance": cb_test,
        "AD_CITYBLOCK_Threshold": cb_threshold,
        "AD_DENSITY_KNN_Distance": density_test,
        "AD_DENSITY_KNN_Threshold": density_threshold,
        "AD_DENSITY_K": k_density,
        "AD_DescriptorsUsed": X_train.shape[1],
        "AD_TrainingRows": X_train.shape[0],
    })



def find_common_ad_training_csv(cli_path: Optional[str] = None) -> Optional[Path]:
    """Find the single common AD training CSV.

    Priority:
    1) --ad-training-csv path, if provided
    2) training_AD.csv in the project root
    3) common names in the project root or training_descriptors/

    This replaces the previous workflow that used independent regression and
    classification AD training files.
    """
    candidates: List[Path] = []
    if cli_path:
        candidates.append(Path(cli_path).expanduser())

    names = [
        "training_AD.csv",
        "training_common_AD.csv",
        "train_AD.csv",
        "common_AD_train.csv",
        "AD_training.csv",
    ]
    candidates.append(TRAINING_AD_FILE)
    for name in names:
        candidates.append(ROOT_DIR / name)
        candidates.append(TRAINING_DESCRIPTORS_DIR / name)

    seen = set()
    for candidate in candidates:
        candidate = candidate if candidate.is_absolute() else (ROOT_DIR / candidate)
        key = str(candidate.resolve()) if candidate.exists() else str(candidate)
        if key in seen:
            continue
        seen.add(key)
        if candidate.exists() and candidate.is_file() and candidate.suffix.lower() == ".csv":
            return candidate
    return None


def clean_ad_training_columns(train_df: pd.DataFrame) -> List[str]:
    """Return descriptor columns from the AD training table.

    ID-like columns are removed. Obvious response/target columns are removed too,
    which makes the function robust if the user accidentally leaves pEXP, class,
    y, target, etc. in the CSV.
    """
    train = make_dataframe_columns_unique(train_df)
    cols = descriptor_columns(train)
    cols = [c for c in cols if str(c) not in TARGET_NAME_GUESSES]

    # Extra protection: sometimes the response is the last column with a name that
    # was not in TARGET_NAME_GUESSES. If it is non-numeric or categorical, remove it.
    if cols:
        last = cols[-1]
        s = train[last]
        numeric_fraction = pd.to_numeric(s, errors="coerce").notna().mean()
        unique_values = pd.Series(s).dropna().astype(str).str.upper().unique().tolist()
        looks_categorical_target = any(v in {"ALTA", "BAJA", "HIGH", "LOW", "ACTIVE", "INACTIVE"} for v in unique_values)
        if numeric_fraction < 0.8 or looks_categorical_target:
            logging.info("Ignoring last AD training column as likely response/target: %s", last)
            cols = cols[:-1]

    if not cols:
        raise ValueError("No descriptor columns were found in the common AD training CSV.")
    return cols


def select_common_ad_matrices(
    train_df: pd.DataFrame,
    external_df: pd.DataFrame,
    strict: bool = True,
) -> Tuple[pd.DataFrame, pd.DataFrame, List[str], List[str]]:
    """Align external screening descriptors to the exact training column order.

    The AD comparison is made only after reindexing the screening matrix with the
    heading/order from the common training CSV. By default, missing descriptors are
    a hard error because AD should be calculated in the same descriptor space.
    """
    train = make_dataframe_columns_unique(train_df)
    external = make_dataframe_columns_unique(external_df)

    train_cols = clean_ad_training_columns(train)
    duplicated_train_cols = pd.Index(train_cols)[pd.Index(train_cols).duplicated()].tolist()
    if duplicated_train_cols:
        raise ValueError(f"Repeated descriptor columns in AD training CSV: {duplicated_train_cols[:20]}")

    external_cols = set(descriptor_columns(external))
    missing_in_external = [c for c in train_cols if c not in external_cols]
    extra_in_external = [c for c in descriptor_columns(external) if c not in set(train_cols)]

    if missing_in_external:
        msg = (
            "The screening descriptor table is missing descriptor(s) required by the common AD training CSV: "
            f"{missing_in_external[:50]}{' ...' if len(missing_in_external) > 50 else ''}"
        )
        if strict:
            raise ValueError(msg)
        logging.warning("%s. They will be imputed only because --allow-missing-ad was used.", msg)

    if extra_in_external:
        logging.info(
            "Common AD: %s extra screening descriptor columns are not used for AD because they are not in training_AD.csv: %s%s",
            len(extra_in_external), extra_in_external[:20], " ..." if len(extra_in_external) > 20 else "",
        )

    X_train = train.reindex(columns=train_cols)
    X_test = external.reindex(columns=train_cols, fill_value=np.nan)

    ordered_external = external[[c for c in ["Molecule", "SMILES", "source_sdf"] if c in external.columns]].copy()
    ordered_external = pd.concat([ordered_external.reset_index(drop=True), X_test.reset_index(drop=True)], axis=1)
    ordered_external.to_csv(AD_ORDERED_SCREENING_CSV, index=False)
    logging.info("Screening descriptors ordered like AD training CSV saved: %s", AD_ORDERED_SCREENING_CSV)

    return X_train, X_test, train_cols, missing_in_external


def evaluate_common_applicability_domain(
    external_descriptors_df: pd.DataFrame,
    require_ad: bool = True,
    training_csv: Optional[str] = None,
) -> Optional[pd.DataFrame]:
    train_csv = find_common_ad_training_csv(training_csv)
    if train_csv is None:
        msg = "Common AD cannot be calculated because training_AD.csv was not found."
        if require_ad:
            fail(msg)
        logging.warning(msg)
        return None

    try:
        train_df = pd.read_csv(train_csv)
        strict = require_ad
        X_train, X_test, used_cols, missing_cols = select_common_ad_matrices(train_df, external_descriptors_df, strict=strict)
        molecule_names = external_descriptors_df["Molecule"].tolist() if "Molecule" in external_descriptors_df.columns else list(range(1, len(external_descriptors_df) + 1))
        ad_df = compute_applicability_domain(X_train, X_test, molecule_names)
        ad_df.insert(0, "AD_Type", "common")
        ad_df.insert(1, "TrainingCSV", str(train_csv))
        ad_df["AD_ColumnsFromTrainingCSV"] = len(clean_ad_training_columns(train_df))
        ad_df["AD_ColumnsMatchedExternal"] = len(used_cols) - len(missing_cols)
        ad_df["AD_MissingExternalColumns"] = ";".join(map(str, missing_cols[:200]))
        ad_df["AD_ColumnOrderSource"] = "training_AD.csv heading/order"
        logging.info(
            "Common AD calculated using %s with %s descriptor columns in training order.",
            train_csv, len(used_cols),
        )
        return ad_df
    except Exception as exc:
        msg = f"Common AD failed: {exc}"
        if require_ad:
            fail(msg)
        logging.warning(msg)
        return None


def filter_common_in_domain(descriptors_df: pd.DataFrame, ad_df: Optional[pd.DataFrame], allow_missing_ad: bool = False) -> pd.DataFrame:
    if ad_df is None:
        if allow_missing_ad:
            logging.warning("Prediction will continue without AD filtering because --allow-missing-ad was used.")
            return descriptors_df.copy()
        fail("Prediction stopped because common AD results are missing.")

    ad_small = ad_df[["Molecule", "AD_InDomain", "AD_OutCount"]].copy()
    merged = descriptors_df.merge(ad_small, on="Molecule", how="left")
    in_mask = merged["AD_InDomain"].astype(str).str.lower().isin(["true", "1.0", "1"])

    outside_cols = [c for c in ["Molecule", "SMILES", "AD_InDomain", "AD_OutCount"] if c in merged.columns]
    outside = merged.loc[~in_mask, outside_cols].copy()
    outside.to_csv(OUTSIDE_AD_CSV, index=False)

    in_df = descriptors_df.loc[in_mask.values].reset_index(drop=True)
    in_df.to_csv(DESCRIPTORS_IN_AD_CSV, index=False)
    logging.info("Common AD filter: %s IN / %s total. IN descriptors saved to %s. Outside molecules saved to %s.", len(in_df), len(descriptors_df), DESCRIPTORS_IN_AD_CSV, OUTSIDE_AD_CSV)
    if in_df.empty:
        fail("No molecules are inside the common applicability domain. No prediction will be generated.")
    return in_df


def merge_ad_to_result(result_df: pd.DataFrame, ad_df: Optional[pd.DataFrame]) -> pd.DataFrame:
    if ad_df is None or ad_df.empty:
        return result_df
    ad_small = ad_df[["Molecule", "AD_InDomain", "AD_OutCount"]].copy()
    return result_df.merge(ad_small, on="Molecule", how="left")


