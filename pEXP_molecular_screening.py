def _safe_list_text(value: Any) -> str:
    """Compact representation for audit CSVs."""
    if value is None:
        return ""
    try:
        if isinstance(value, np.ndarray):
            value = value.tolist()
        if isinstance(value, (list, tuple, pd.Index)):
            return ";".join(map(str, list(value)))
        return str(value)
    except Exception:
        return str(value)


def inverse_transform_labels(raw_pred: np.ndarray, bundle: Dict[str, Any], estimator: Any) -> np.ndarray:
    """Convert numeric encoded predictions back to the original labels when possible."""
    le = bundle.get("label_encoder")
    if le is not None:
        try:
            return le.inverse_transform(raw_pred.astype(int))
        except Exception:
            pass
    return raw_pred.astype(str)


def get_probability_labels(bundle: Dict[str, Any], estimator: Any, pred_labels: Optional[np.ndarray] = None) -> List[str]:
    """Return labels in the exact column order used by predict_proba.

    This is important because sklearn guarantees that predict_proba columns follow
    estimator.classes_. If the estimator was trained on encoded labels 0/1 and a
    LabelEncoder is stored in the bundle, the estimator.classes_ are converted back
    through that LabelEncoder while preserving the estimator.classes_ order.
    """
    classes = getattr(estimator, "classes_", None)
    le = bundle.get("label_encoder")

    if classes is not None:
        classes_arr = np.asarray(classes)
        if le is not None:
            try:
                return [str(x) for x in le.inverse_transform(classes_arr.astype(int))]
            except Exception:
                pass
        return [str(x) for x in classes_arr.tolist()]

    if "class_labels" in bundle:
        return [str(x) for x in list(bundle["class_labels"])]

    if le is not None and hasattr(le, "classes_"):
        return [str(x) for x in list(le.classes_)]

    if pred_labels is not None:
        return sorted([str(x) for x in pd.Series(pred_labels).dropna().unique().tolist()])
    return []


def choose_positive_class_index(labels: Sequence[str], requested_positive_class: Optional[str] = DEFAULT_POSITIVE_CLASS) -> int:
    """Select the probability column used for Score/DP.

    The workflow uses ALTA as positive class by default. This prevents old bundles
    saved with positive_class='BAJA' from making DP positive for BAJA by accident.
    """
    labels_list = [str(x) for x in labels]
    if requested_positive_class:
        requested = str(requested_positive_class).strip()
        for i, label in enumerate(labels_list):
            if label == requested or label.lower() == requested.lower():
                return i
        logging.warning(
            "Requested positive class '%s' was not found among probability labels %s. Falling back to automatic selection.",
            requested,
            labels_list,
        )

    for preferred in ["ALTA", "Alta", "alta", "HIGH", "High", "high", "1"]:
        if preferred in labels_list:
            return labels_list.index(preferred)
    if len(labels_list) >= 2:
        return 1
    return 0


def _sigmoid(values: np.ndarray) -> np.ndarray:
    values = np.asarray(values, dtype=float)
    values = np.clip(values, -500, 500)
    return 1.0 / (1.0 + np.exp(-values))


def _softmax(values: np.ndarray) -> np.ndarray:
    values = np.asarray(values, dtype=float)
    values = values - np.max(values, axis=1, keepdims=True)
    exp_values = np.exp(values)
    denom = exp_values.sum(axis=1, keepdims=True)
    denom[denom == 0] = 1.0
    return exp_values / denom


def classifier_scores(
    estimator: Any,
    X: pd.DataFrame,
    labels: Sequence[str],
    pos_idx: int,
) -> Tuple[Optional[np.ndarray], Optional[np.ndarray], str]:
    """Return probability matrix and score for the selected positive class.

    score_pos is always aligned with labels[pos_idx]. For binary classifiers without
    predict_proba, sklearn decision_function is usually oriented to classes_[1];
    if the selected positive class is classes_[0], the score is inverted as 1-score.
    """
    if hasattr(estimator, "predict_proba"):
        try:
            probs = np.asarray(estimator.predict_proba(X), dtype=float)
            if probs.ndim == 2:
                score_pos = probs[:, pos_idx] if probs.shape[1] > pos_idx else probs[:, -1]
                return probs, score_pos, "predict_proba"
        except Exception as exc:
            logging.warning("predict_proba failed and decision_function will be tried if available: %s", exc)

    if hasattr(estimator, "decision_function"):
        try:
            scores = np.asarray(estimator.decision_function(X), dtype=float)
            if scores.ndim == 1:
                prob_class_1 = _sigmoid(scores)
                if len(labels) == 2 and pos_idx == 0:
                    return None, 1.0 - prob_class_1, "decision_function_binary_inverted"
                return None, prob_class_1, "decision_function_binary"
            probs_like = _softmax(scores)
            score_pos = probs_like[:, pos_idx] if probs_like.shape[1] > pos_idx else probs_like[:, -1]
            return probs_like, score_pos, "decision_function_softmax"
        except Exception as exc:
            logging.warning("decision_function failed: %s", exc)

    return None, None, "none"


def append_classification_training_audits(
    model_path: Path,
    estimator: Any,
    bundle: Dict[str, Any],
    train_csv: Optional[Path],
    X_screen: pd.DataFrame,
    descriptors_in_df: pd.DataFrame,
    positive_class: str,
) -> None:
    """Create optional training self-check and nearest-training-row audits.

    This does not retrain or alter the saved model. It verifies whether the saved
    classifier can reproduce its own training CSV labels and whether screened
    molecules are descriptor-identical or very close to training rows.
    """
    if not WRITE_AUDIT_FILES:
        return

    if train_csv is None or not train_csv.exists():
        return

    try:
        X_train, y_train, target_col = prepare_training_X_y_for_model(
            train_csv,
            model_path,
            estimator,
            bundle,
            X_screen.columns.tolist(),
        )
    except Exception as exc:
        logging.warning("Classification training audit failed while preparing %s: %s", train_csv, exc)
        return

    if y_train is not None:
        try:
            pred_train_raw = np.asarray(estimator.predict(X_train))
            pred_train = inverse_transform_labels(pred_train_raw, bundle, estimator).astype(str)
            y_true = np.asarray(y_train).astype(str)
            ok = pred_train == y_true
            train_df = make_dataframe_columns_unique(pd.read_csv(train_csv))
            id_col = first_id_column(train_df)
            selfcheck = pd.DataFrame({
                "ModelFile": str(model_path),
                "TrainingCSV": str(train_csv),
                "TrainingRow": np.arange(len(train_df)),
                "TrainingID": train_df[id_col].astype(str).values if id_col else np.arange(len(train_df)).astype(str),
                "TargetColumn": target_col if target_col else "",
                "Class_True": y_true,
                "Class_Predicted_BySavedModel": pred_train,
                "Correct": ok,
            })
            try:
                labels = get_probability_labels(bundle, estimator, pred_train)
                pos_idx = choose_positive_class_index(labels, positive_class) if labels else 0
                probs, score_pos, score_source = classifier_scores(estimator, X_train, labels, pos_idx)
                if probs is not None and labels:
                    for j in range(probs.shape[1]):
                        label = labels[j] if j < len(labels) else f"class_{j}"
                        selfcheck[f"Prob_{label}"] = probs[:, j]
                if score_pos is not None:
                    selfcheck[f"Score_{sanitize_name(labels[pos_idx]) if labels else sanitize_name(positive_class)}"] = score_pos
                    selfcheck["ScoreSource"] = score_source
            except Exception as exc:
                logging.warning("Could not add train probabilities to self-check: %s", exc)

            selfcheck.to_csv(CLASSIFICATION_TRAIN_SELF_CHECK_CSV, index=False)
            logging.info(
                "Classification training self-check saved: %s | accuracy on provided train CSV = %.4f (%s/%s)",
                CLASSIFICATION_TRAIN_SELF_CHECK_CSV,
                float(ok.mean()) if len(ok) else float("nan"),
                int(ok.sum()),
                len(ok),
            )
        except Exception as exc:
            logging.warning("Classification training self-check failed for %s: %s", model_path.name, exc)

    try:
        train_df = make_dataframe_columns_unique(pd.read_csv(train_csv))
        id_col = first_id_column(train_df)
        Xtr = X_train.to_numpy(dtype=float)
        Xsc = X_screen.to_numpy(dtype=float)
        if Xtr.size == 0 or Xsc.size == 0:
            return
        mean = Xtr.mean(axis=0)
        std = Xtr.std(axis=0)
        std[std == 0] = 1.0
        Ztr = (Xtr - mean) / std
        Zsc = (Xsc - mean) / std
        rows = []
        for i in range(Zsc.shape[0]):
            diff = Ztr - Zsc[i, :]
            eu = np.sqrt((diff ** 2).sum(axis=1))
            nearest = int(np.argmin(eu))
            raw_abs = np.abs(Xtr[nearest, :] - Xsc[i, :])
            max_abs = float(np.nanmax(raw_abs)) if raw_abs.size else float("nan")
            nearest_target = ""
            if target_col and target_col in train_df.columns:
                nearest_target = str(labels_as_strings([train_df.loc[nearest, target_col]], bundle)[0])
            rows.append({
                "ModelFile": str(model_path),
                "TrainingCSV": str(train_csv),
                "Molecule": descriptors_in_df.iloc[i]["Molecule"] if "Molecule" in descriptors_in_df.columns else i,
                "SMILES": descriptors_in_df.iloc[i]["SMILES"] if "SMILES" in descriptors_in_df.columns else "",
                "NearestTrainingRow": nearest,
                "NearestTrainingID": str(train_df.loc[nearest, id_col]) if id_col else str(nearest),
                "NearestTrainingTarget": nearest_target,
                "NearestTrainingEuclideanZ": float(eu[nearest]),
                "NearestTrainingMaxAbsDescriptorDiff": max_abs,
                "ExactDescriptorMatchTol1e-8": bool(max_abs <= 1e-8),
            })
        pd.DataFrame(rows).to_csv(CLASSIFICATION_TRAIN_MATCH_AUDIT_CSV, index=False)
        logging.info("Classification nearest-training-row audit saved: %s", CLASSIFICATION_TRAIN_MATCH_AUDIT_CSV)
    except Exception as exc:
        logging.warning("Classification nearest-training-row audit failed for %s: %s", model_path.name, exc)


def run_classification_screening(
    descriptors_in_df: pd.DataFrame,
    classification_ad_df: Optional[pd.DataFrame],
    positive_class: str = DEFAULT_POSITIVE_CLASS,
    use_model_train_csv: bool = True,
    append_results: bool = False,
    chunk_index: int = 0,
) -> Optional[pd.DataFrame]:
    model_files = ordered_skl_files(CLASSIFICATION_MODELS_DIR)
    if not model_files:
        logging.warning("No .skl classification models found in: %s", CLASSIFICATION_MODELS_DIR)
        return None

    out = descriptors_in_df[["Molecule"]].copy()
    if "SMILES" in descriptors_in_df.columns:
        out["SMILES"] = descriptors_in_df["SMILES"].values

    feature_audit_rows: List[Dict[str, Any]] = []
    label_audit_rows: List[Dict[str, Any]] = []

    for model_path in model_files:
        model_name = model_path.stem
        logging.info("Predicting classification model: %s", model_path.name)
        estimator, bundle = load_skl(model_path)
        model_train_csv = find_model_training_csv(model_path, allow_unique_fallback=True) if use_model_train_csv else None
        if model_train_csv is not None:
            logging.info("%s: matched classification training CSV: %s", model_path.name, model_train_csv)
        X = select_features_for_model(
            descriptors_in_df,
            model_path,
            estimator,
            bundle,
            allow_dummies=True,
            model_train_csv=model_train_csv,
        )
        if not append_results or chunk_index == 0:
            append_classification_training_audits(
                model_path,
                estimator,
                bundle,
                model_train_csv,
                X,
                descriptors_in_df,
                positive_class,
            )
        feature_audit_rows.append({
            "ModelType": "classification",
            "ModelFile": str(model_path),
            "OutputColumn": "Class_Predicted" if len(model_files) == 1 else f"{model_name}_Class_Predicted",
            "NFeaturesUsed": X.shape[1],
            "ExpectedNFeatures": get_expected_n_features(estimator),
            "TrainingCSVUsed": str(model_train_csv) if model_train_csv is not None else "",
            "FeatureSourceColumns": ";".join(map(str, X.columns.tolist())),
        })

        try:
            pred_raw = estimator.predict(X)
        except Exception as exc:
            fail(f"Prediction failed for classification model {model_path.name}: {exc}")

        pred_raw = np.asarray(pred_raw)
        pred_labels = inverse_transform_labels(pred_raw, bundle, estimator)
        labels = get_probability_labels(bundle, estimator, pred_labels)
        pos_idx = choose_positive_class_index(labels, positive_class) if labels else 0
        positive_label = labels[pos_idx] if labels and pos_idx < len(labels) else str(positive_class or "Positive")

        le = bundle.get("label_encoder")
        label_audit_rows.append({
            "ModelFile": str(model_path),
            "EstimatorClassesRaw": _safe_list_text(getattr(estimator, "classes_", None)),
            "LabelEncoderClasses": _safe_list_text(getattr(le, "classes_", None) if le is not None else None),
            "BundleClassLabels": _safe_list_text(bundle.get("class_labels")),
            "BundlePositiveClass": _safe_list_text(bundle.get("positive_class")),
            "ProbabilityLabelsUsed": _safe_list_text(labels),
            "RequestedPositiveClass": positive_class,
            "SelectedPositiveClass": positive_label,
            "SelectedPositiveIndex": pos_idx,
        })

        single_model = len(model_files) == 1
        if single_model:
            out["Class_Predicted"] = pred_labels
        else:
            out[f"{model_name}_Class_Predicted"] = pred_labels

        probs, score_pos, score_source = classifier_scores(estimator, X, labels, pos_idx)
        if probs is not None and labels and probs.ndim == 2:
            for j in range(probs.shape[1]):
                label = labels[j] if j < len(labels) else f"class_{j}"
                col_name = f"Prob_{label}" if single_model else f"{model_name}_Prob_{label}"
                out[col_name] = probs[:, j]

        if score_pos is not None:
            # Explicit columns tied to the selected class. With the default settings these are Score_ALTA and DP_ALTA.
            safe_positive = sanitize_name(positive_label)
            explicit_score_col = f"Score_{safe_positive}" if single_model else f"{model_name}_Score_{safe_positive}"
            explicit_dp_col = f"DP_{safe_positive}" if single_model else f"{model_name}_DP_{safe_positive}"
            pos_col = "PositiveClass" if single_model else f"{model_name}_PositiveClass"

            out[explicit_score_col] = score_pos
            out[explicit_dp_col] = (2.0 * score_pos - 1.0) * 100.0

            # Backward-compatible aliases. They now have the same orientation as PositiveClass.
            score_col = "Score_PositiveClass" if single_model else f"{model_name}_Score_PositiveClass"
            dp_col = "DP" if single_model else f"{model_name}_DP"
            out[score_col] = score_pos
            out[dp_col] = (2.0 * score_pos - 1.0) * 100.0
            out[pos_col] = positive_label

            label_audit_rows[-1]["ScoreSource"] = score_source
            label_audit_rows[-1]["ScoreColumn"] = explicit_score_col
            label_audit_rows[-1]["DPColumn"] = explicit_dp_col

    if WRITE_AUDIT_FILES and feature_audit_rows and (not append_results or chunk_index == 0):
        pd.DataFrame(feature_audit_rows).to_csv(CLASSIFICATION_MODEL_FEATURE_AUDIT_CSV, index=False)
        logging.info("Classification model feature audit saved: %s", CLASSIFICATION_MODEL_FEATURE_AUDIT_CSV)

    if WRITE_AUDIT_FILES and label_audit_rows and (not append_results or chunk_index == 0):
        pd.DataFrame(label_audit_rows).to_csv(CLASSIFICATION_LABEL_AUDIT_CSV, index=False)
        logging.info("Classification label audit saved: %s", CLASSIFICATION_LABEL_AUDIT_CSV)

    out = merge_ad_to_result(out, classification_ad_df)
    id_cols = [c for c in ["Molecule", "SMILES"] if c in out.columns]
    priority = [c for c in ["Class_Predicted", "AD_InDomain", "AD_OutCount"] if c in out.columns]
    score_priority = [c for c in [f"Score_{sanitize_name(positive_class)}", f"DP_{sanitize_name(positive_class)}", "PositiveClass"] if c in out.columns]
    other = [c for c in out.columns if c not in id_cols + priority + score_priority]
    out = out[id_cols + priority + score_priority + other]
    append_dataframe_csv(out, RESULTS_CLASS_CSV, first_chunk=(not append_results or chunk_index == 0))
    logging.info("Classification results saved/appended: %s", RESULTS_CLASS_CSV)
    return out



