class CommonADChunkEvaluator:
    """Memory-safe common applicability-domain evaluator.

    The training domain is fitted once from training_AD.csv. Screening molecules
    are then transformed and evaluated in chunks read from descriptors_merged.csv.
    This avoids creating a full z_test array or pairwise distance tensor for more
    than one million molecules.
    """

    def __init__(self, train_df: pd.DataFrame, training_csv: Path, n_jobs: int = 1) -> None:
        require_sklearn_neighbors()
        self.training_csv = training_csv
        self.n_jobs = int(n_jobs)
        train_df = make_dataframe_columns_unique(train_df)
        self.train_cols = clean_ad_training_columns(train_df)

        train = train_df.reindex(columns=self.train_cols)
        train = train.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)
        self.train_medians = train.median(numeric_only=True).reindex(self.train_cols).fillna(DEFAULT_FILL_VALUE)
        train = train.fillna(self.train_medians).fillna(DEFAULT_FILL_VALUE)

        self.keep_cols = train.columns[train.nunique(dropna=False) > 1].tolist()
        if train.shape[0] < 2 or len(self.keep_cols) < 1:
            raise ValueError(f"AD training table is too small or has no variable descriptors: rows={train.shape[0]}, variable_descriptors={len(self.keep_cols)}")

        train = train[self.keep_cols].copy()
        self.train_min = train.min(axis=0)
        self.train_max = train.max(axis=0)
        self.mean = train.mean(axis=0).to_numpy(dtype=np.float64)
        self.std = train.std(axis=0, ddof=0).replace(0, 1).to_numpy(dtype=np.float64)
        z_train = ((train.to_numpy(dtype=np.float64) - self.mean) / self.std).astype(np.float64, copy=False)
        self.z_train = z_train

        # Training thresholds use leave-one-out neighbors: the nearest neighbor is
        # the molecule itself, so we ask for 2 neighbors for 1-NN thresholds and
        # k+1 neighbors for density thresholds.
        n_train = z_train.shape[0]
        n_jobs = self.n_jobs
        self.k_density = max(1, min(AD_K_NEIGHBORS, n_train - 1))

        self.nn_euclidean = NearestNeighbors(n_neighbors=1, metric="euclidean", algorithm="auto", n_jobs=n_jobs)
        self.nn_euclidean.fit(z_train)
        train_nn_eu = NearestNeighbors(n_neighbors=min(2, n_train), metric="euclidean", algorithm="auto", n_jobs=n_jobs)
        train_nn_eu.fit(z_train)
        eu_train_dist = train_nn_eu.kneighbors(z_train, return_distance=True)[0]
        eu_train_nn = eu_train_dist[:, 1] if eu_train_dist.shape[1] > 1 else eu_train_dist[:, 0]
        self.eu_threshold = safe_percentile(eu_train_nn, AD_DISTANCE_PERCENTILE)

        self.nn_cityblock = NearestNeighbors(n_neighbors=1, metric="cityblock", algorithm="auto", n_jobs=n_jobs)
        self.nn_cityblock.fit(z_train)
        train_nn_cb = NearestNeighbors(n_neighbors=min(2, n_train), metric="cityblock", algorithm="auto", n_jobs=n_jobs)
        train_nn_cb.fit(z_train)
        cb_train_dist = train_nn_cb.kneighbors(z_train, return_distance=True)[0]
        cb_train_nn = cb_train_dist[:, 1] if cb_train_dist.shape[1] > 1 else cb_train_dist[:, 0]
        self.cb_threshold = safe_percentile(cb_train_nn, AD_DISTANCE_PERCENTILE)

        self.nn_density = NearestNeighbors(n_neighbors=self.k_density, metric="euclidean", algorithm="auto", n_jobs=n_jobs)
        self.nn_density.fit(z_train)
        train_nn_den = NearestNeighbors(n_neighbors=min(self.k_density + 1, n_train), metric="euclidean", algorithm="auto", n_jobs=n_jobs)
        train_nn_den.fit(z_train)
        den_train_dist = train_nn_den.kneighbors(z_train, return_distance=True)[0]
        den_col = min(self.k_density, den_train_dist.shape[1] - 1)
        density_train = den_train_dist[:, den_col]
        self.density_threshold = safe_percentile(density_train, AD_DISTANCE_PERCENTILE)

        logging.info(
            "Common AD fitted from %s | train rows=%s | descriptors used=%s | thresholds: euclidean=%.6g, cityblock=%.6g, density=%.6g",
            training_csv, n_train, len(self.keep_cols), self.eu_threshold, self.cb_threshold, self.density_threshold,
        )

    def _prepare_test(self, external_df: pd.DataFrame, strict: bool = True) -> Tuple[pd.DataFrame, List[str]]:
        external = make_dataframe_columns_unique(external_df)
        external_cols = set(descriptor_columns(external))
        missing = [c for c in self.train_cols if c not in external_cols]
        if missing and strict:
            raise ValueError(
                "The screening descriptor table is missing descriptor(s) required by the common AD training CSV: "
                f"{missing[:50]}{' ...' if len(missing) > 50 else ''}"
            )
        if missing:
            logging.warning("Common AD chunk has missing descriptor(s); they will be imputed: %s", missing[:20])
        test = external.reindex(columns=self.train_cols, fill_value=np.nan)
        test = test.apply(pd.to_numeric, errors="coerce").replace([np.inf, -np.inf], np.nan)
        test = test.fillna(self.train_medians).fillna(DEFAULT_FILL_VALUE)
        test = test[self.keep_cols].copy()
        return test, missing

    def evaluate(self, external_df: pd.DataFrame, strict: bool = True) -> pd.DataFrame:
        test, missing = self._prepare_test(external_df, strict=strict)
        n_test = len(test)
        if n_test == 0:
            return pd.DataFrame()

        molecule_names = external_df["Molecule"].tolist() if "Molecule" in external_df.columns else list(range(1, n_test + 1))
        inside = test.ge(self.train_min, axis=1) & test.le(self.train_max, axis=1)
        range_fraction = inside.mean(axis=1).to_numpy(dtype=np.float64)
        range_out = range_fraction < AD_RANGE_MIN_FRACTION

        z_test = ((test.to_numpy(dtype=np.float64) - self.mean) / self.std).astype(np.float64, copy=False)
        eu_test = self.nn_euclidean.kneighbors(z_test, return_distance=True)[0][:, 0]
        cb_test = self.nn_cityblock.kneighbors(z_test, return_distance=True)[0][:, 0]
        density_distances = self.nn_density.kneighbors(z_test, return_distance=True)[0]
        density_test = density_distances[:, self.k_density - 1] if density_distances.ndim == 2 else density_distances

        flags = pd.DataFrame({
            "AD_RANGE_Out": range_out.astype(float),
            "AD_EUCLIDEAN_Out": (eu_test > self.eu_threshold).astype(float) if np.isfinite(self.eu_threshold) else np.full(n_test, np.nan),
            "AD_CITYBLOCK_Out": (cb_test > self.cb_threshold).astype(float) if np.isfinite(self.cb_threshold) else np.full(n_test, np.nan),
            "AD_DENSITY_Out": (density_test > self.density_threshold).astype(float) if np.isfinite(self.density_threshold) else np.full(n_test, np.nan),
        })
        out_count = flags.sum(axis=1, skipna=True)
        valid_counts = flags.notna().sum(axis=1)
        in_domain = np.where(valid_counts >= 1, out_count < AD_OUT_COUNT_THRESHOLD, np.nan)

        return pd.DataFrame({
            "AD_Type": "common",
            "TrainingCSV": str(self.training_csv),
            "Molecule": molecule_names,
            "AD_InDomain": in_domain,
            "AD_OutCount": out_count,
            "AD_RANGE_Out": flags["AD_RANGE_Out"],
            "AD_EUCLIDEAN_Out": flags["AD_EUCLIDEAN_Out"],
            "AD_CITYBLOCK_Out": flags["AD_CITYBLOCK_Out"],
            "AD_DENSITY_Out": flags["AD_DENSITY_Out"],
            "AD_RANGE_FractionInside": range_fraction,
            "AD_EUCLIDEAN_Distance": eu_test,
            "AD_EUCLIDEAN_Threshold": self.eu_threshold,
            "AD_CITYBLOCK_Distance": cb_test,
            "AD_CITYBLOCK_Threshold": self.cb_threshold,
            "AD_DENSITY_KNN_Distance": density_test,
            "AD_DENSITY_KNN_Threshold": self.density_threshold,
            "AD_DENSITY_K": self.k_density,
            "AD_DescriptorsUsed": len(self.keep_cols),
            "AD_TrainingRows": self.z_train.shape[0],
            "AD_ColumnsFromTrainingCSV": len(self.train_cols),
            "AD_ColumnsMatchedExternal": len(self.train_cols) - len(missing),
            "AD_MissingExternalColumns": ";".join(map(str, missing[:200])),
            "AD_ColumnOrderSource": "training_AD.csv heading/order",
        })


def fit_common_ad_chunk_evaluator(require_ad: bool = True, training_csv: Optional[str] = None, n_jobs: int = 1) -> Optional[CommonADChunkEvaluator]:
    train_csv = find_common_ad_training_csv(training_csv)
    if train_csv is None:
        msg = "Common AD cannot be calculated because training_AD.csv was not found."
        if require_ad:
            fail(msg)
        logging.warning(msg)
        return None
    try:
        train_df = pd.read_csv(train_csv, low_memory=False)
        return CommonADChunkEvaluator(train_df, train_csv, n_jobs=n_jobs)
    except Exception as exc:
        msg = f"Common AD setup failed: {exc}"
        if require_ad:
            fail(msg)
        logging.warning(msg)
        return None


def filter_common_in_domain_chunk(
    descriptors_chunk: pd.DataFrame,
    ad_chunk: Optional[pd.DataFrame],
    allow_missing_ad: bool = False,
    first_chunk: bool = False,
) -> pd.DataFrame:
    if ad_chunk is None:
        if allow_missing_ad:
            logging.warning("Prediction chunk will continue without AD filtering because --allow-missing-ad was used.")
            append_dataframe_csv(descriptors_chunk, DESCRIPTORS_IN_AD_CSV, first_chunk=first_chunk)
            return descriptors_chunk.copy()
        fail("Prediction stopped because common AD results are missing.")

    ad_small = ad_chunk[["Molecule", "AD_InDomain", "AD_OutCount"]].copy()
    merged = descriptors_chunk.merge(ad_small, on="Molecule", how="left")
    in_mask = merged["AD_InDomain"].astype(str).str.lower().isin(["true", "1.0", "1"])

    outside_cols = [c for c in ["Molecule", "SMILES", "AD_InDomain", "AD_OutCount"] if c in merged.columns]
    outside = merged.loc[~in_mask, outside_cols].copy()
    append_dataframe_csv(outside, OUTSIDE_AD_CSV, first_chunk=first_chunk)

    in_df = descriptors_chunk.loc[in_mask.values].reset_index(drop=True)
    append_dataframe_csv(in_df, DESCRIPTORS_IN_AD_CSV, first_chunk=first_chunk)
    return in_df


def run_large_screening_from_merged_csv(
    require_ad: bool,
    training_csv: Optional[str],
    chunk_size: int,
    ad_only: bool,
    allow_missing_ad: bool,
    positive_class: str,
    use_model_train_csv: bool,
    ad_n_jobs: int = 1,
) -> None:
    if not MERGED_DESCRIPTORS_CSV.exists():
        fail(f"Merged descriptor CSV not found: {MERGED_DESCRIPTORS_CSV}. Run without --use-existing-descriptors first.")

    remove_existing_outputs([
        AD_COMMON_CSV, OUTSIDE_AD_CSV, DESCRIPTORS_IN_AD_CSV,
        INDIVIDUAL_REGRESSION_CSV, ENSEMBLE_INPUT_CSV,
        RESULTS_REG_CSV, RESULTS_CLASS_CSV,
        REGRESSION_MODEL_FEATURE_AUDIT_CSV, CLASSIFICATION_MODEL_FEATURE_AUDIT_CSV,
        CLASSIFICATION_LABEL_AUDIT_CSV, CLASSIFICATION_TRAIN_SELF_CHECK_CSV,
        CLASSIFICATION_TRAIN_MATCH_AUDIT_CSV,
    ])

    ad_evaluator = fit_common_ad_chunk_evaluator(require_ad=require_ad, training_csv=training_csv, n_jobs=ad_n_jobs)
    total_rows = 0
    total_in = 0
    total_out = 0
    chunks_with_predictions = 0

    logging.info("Starting large-screening chunk mode from %s | chunk_size=%s", MERGED_DESCRIPTORS_CSV, chunk_size)
    reader = pd.read_csv(MERGED_DESCRIPTORS_CSV, chunksize=max(1, int(chunk_size)), low_memory=False)
    for chunk_index, chunk in enumerate(reader):
        chunk = make_dataframe_columns_unique(chunk)
        total_rows += len(chunk)
        logging.info("Processing chunk %s | rows=%s | total_rows=%s", chunk_index + 1, len(chunk), total_rows)

        if ad_evaluator is not None:
            ad_chunk = ad_evaluator.evaluate(chunk, strict=require_ad)
        else:
            molecule_names = chunk["Molecule"].tolist() if "Molecule" in chunk.columns else list(range(total_rows - len(chunk) + 1, total_rows + 1))
            ad_chunk = pd.DataFrame({
                "AD_Type": "common_missing",
                "TrainingCSV": "",
                "Molecule": molecule_names,
                "AD_InDomain": True,
                "AD_OutCount": 0,
            })

        append_dataframe_csv(ad_chunk, AD_COMMON_CSV, first_chunk=(chunk_index == 0))
        descriptors_in = filter_common_in_domain_chunk(
            chunk,
            ad_chunk,
            allow_missing_ad=allow_missing_ad,
            first_chunk=(chunk_index == 0),
        )
        n_in = len(descriptors_in)
        n_out = len(chunk) - n_in
        total_in += n_in
        total_out += n_out
        logging.info("Chunk %s common AD: %s IN / %s OUT", chunk_index + 1, n_in, n_out)

        if ad_only:
            del chunk, ad_chunk, descriptors_in
            gc.collect()
            continue

        if n_in > 0:
            run_regression_screening(
                descriptors_in,
                ad_chunk,
                use_model_train_csv=use_model_train_csv,
                append_results=True,
                chunk_index=chunks_with_predictions,
            )
            run_classification_screening(
                descriptors_in,
                ad_chunk,
                positive_class=positive_class,
                use_model_train_csv=use_model_train_csv,
                append_results=True,
                chunk_index=chunks_with_predictions,
            )
            chunks_with_predictions += 1

        del chunk, ad_chunk, descriptors_in
        gc.collect()

    logging.info(
        "Large-screening chunk mode completed | total=%s | IN=%s | OUT=%s | prediction_chunks=%s",
        total_rows, total_in, total_out, chunks_with_predictions,
    )
    if total_rows == 0:
        fail("No rows were read from descriptors_merged.csv.")
    if (not ad_only) and total_in == 0:
        fail("No molecules are inside the common applicability domain. No prediction was generated.")

