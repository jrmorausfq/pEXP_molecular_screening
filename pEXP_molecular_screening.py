#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
pEXP molecular screening workflow for sklearn models.

Final workflow requested:
1. Read 3D molecules from ToMoCoMD/chemical_datasets/*.sdf.
2. Calculate ToMoCoMD descriptors.
3. Calculate the required PaDEL APC2D descriptors using padelpy and an XML that activates AtomPairs2DFingerprintCount.
4. Calculate selected RDKit descriptors listed in rdkit_descriptors.txt.
5. Merge ToMoCoMD + PaDEL + RDKit descriptors into one descriptor matrix.
6. Calculate one common applicability domain (AD) before prediction using one global training CSV:
      training_AD.csv
   The screening descriptor matrix is reordered to exactly match the descriptor column order in training_AD.csv.
7. Molecules inside this single AD are passed to both regression and classification models.
8. Predict regression and classification only for molecules IN the common AD.

Expected project structure:
.
├── pEXP_molecular_screening_padel_AD.py
├── rdkit_descriptors.txt
├── padel_descriptors.txt  # optional; defaults to APC2D2_C_F, APC2D3_C_F, APC2D1_C_F
├── descriptors.xml        # optional; used to generate the PaDEL APC2D XML
├── training_AD.csv          # common AD training descriptors for regression + classification
├── ToMoCoMD/
│   ├── ToMoCoMD-CARDD_CLI.jar
│   ├── headings.txt
│   └── chemical_datasets/
│       └── *.sdf
└── models/
    ├── regression/
    │   ├── Ensamble.skl          # or Ensambe.skl / Ensemble.skl
    │   ├── Ensamble.txt          # 9 individual prediction columns, in ensemble order
    │   ├── Ensamble_train.csv    # optional; 9 predictions + response, used for ensemble order/medians
    │   └── individuals/
    │       ├── LR_BF_170_MAS.skl
    │       ├── LR_BF_170_MAS.txt
    │       ├── LR_BF_170_MAS_train.csv  # optional; descriptors + response
    │       ├── ... nine individual regression models ...
    │       
    └── classification/
        ├── classifier.skl        # one classification model, no ensemble
        ├── classifier.txt
        

Main outputs are written inside Screening_Results/.

Notes:
- The .skl files can be direct sklearn estimators or joblib dictionaries/bundles.
- Regression bundles generated by the previous script are supported when they contain:
  estimator, feature_columns, train_medians.
- Classification bundles generated by the previous script are supported when they contain:
  model, feature_columns, train_medians, label_encoder, class_labels.
- Each individual .txt file with the same basename as the .skl contains descriptor names or indexes.
- Optional *_train.csv files are used before .txt/bundle feature lists to recover the exact training column order and training medians.
- The ensemble .txt contains the names/order of the individual prediction columns, not molecular descriptors.
- If a classification *_train.csv is present, the script performs a training self-check and nearest-training-row audit.
- If the common AD training CSV is missing, the script stops by default because this workflow requires AD before prediction.
  Use --allow-missing-ad only for debugging.

Large-screening update:
- AD and prediction stages can run in CSV chunks so >1M molecules do not need to stay in RAM.
- Use --use-existing-descriptors to resume from Screening_Results/descriptors_merged.csv after a crash.
"""

